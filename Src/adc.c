float ADC_GetVal(){
	float tmp;
	__disable_irq();
	if (adc.val_count == 0)
		return 0;
	tmp = (adc.val) * K_I / adc.val_count;
	adc.val_count = 0;
	adc.val=0;
	__enable_irq();
	return tmp;
}

float ADC_GetAh(){
	float tmp;
	__disable_irq();
	tmp = adc.summ_ah*K_I/3600.0f/SAMPLE_FREQ;
	__enable_irq();
	return tmp;
}

void ADC_SetAh(float ah){
	__disable_irq();
	adc.summ_ah = ah;
	__enable_irq();
}

float ADC_GetPercent(){
	float tmp;
	__disable_irq();
	if (adc.summ_ah == 0){
		tmp = adc.zero_ah;
	} else{
		tmp = (float)(adc.zero_ah + adc.summ_ah) / (float)adc.zero_ah * 100.0f;
	}
	__enable_irq();
	return tmp;
}
