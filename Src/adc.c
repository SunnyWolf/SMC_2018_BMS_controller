#define ADC_NUM						4

#define SAMPLE_FREQ				5000

#define U_REF							(3.0f)
#define U_SHUNT						(0.075f)
#define I_SHUNT						(300.0f)
#define R_AD620						(1.76f)
#define K_AD620						(49.4f/R_AD620+1.0f)
#define K_I								(U_REF/4095.0f*I_SHUNT/U_SHUNT/K_AD620)

typedef struct {
	int32_t		val;
	int32_t		zero;
	int64_t		summ_ah;
	uint32_t	val_count;
	float			zero_ah;
} T_ADC;

T_ADC adc;
volatile uint16_t adcResult[2] = {0};

void ADC_DMA_Callback(DMA_HandleTypeDef* hdma){
	adc.val += adcResult[0] - adcResult[1];
	adc.val_count++;
	//adc.val += ( ( ((uint32_t)adcResult[0] - (uint32_t)adcResult[1])<<16 ) - adc.val)>>8;
	adc.summ_ah += adc.val;
}

